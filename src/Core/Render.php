<?php


namespace Bedrox\Core;

class Render
{
    public $data;

    /**
     * Render constructor.
     * @param array $data
     */
    public function __construct(array $data)
    {
        $this->data = $data;
    }

    /**
     * @return array
     */
    public function getData(): array
    {
        return $this->data;
    }
}
